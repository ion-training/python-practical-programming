# What is the order of operations?
# 1
print(6 * 3 + 7 * 4) # multiplication then resulting values added
print((6*3) + (7*4))

# 2 
print(5 + 3 / 4) # division then sum between 5 and remaining number
print(5 + (3 / 4))

# 3
print(5-2*3**4) # raise to power then multiplication then substraction
print(5-(2*(3**4)))
